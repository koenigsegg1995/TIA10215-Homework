package hw1;

public class Q1 {

	public static void main(String[] args) {
		int num1 = 12, num2 = 6; //宣告兩個變數，存放題目兩個數字
		int add, multi; //宣告兩個變數代表加法和乘法答案
		
		add = num1 + num2; //相加並指定給 add 存放
		
		multi = num1 * num2; //加成並指定給 multi 存放
		
		System.out.println("兩者的和為" + add); //印出兩者的和
		System.out.println("兩者的積為" + multi); //印出兩者的積
	}
}
